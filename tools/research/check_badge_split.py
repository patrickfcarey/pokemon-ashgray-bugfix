def txt(o,n=34):
    if o is None or o>=len(ag): return ''
    s=''
    for b in ag[o:o+n]:
        if b==0xFF: break
        s+=cs.get(b,'')
    return s.strip()
def pf(o):
    v=struct.unpack('<I',ag[o:o+4])[0]; return v-ROM if 0x08000000<=v<0x08000000+len(ag) else None
def aligned_start(S):
    # find the furthest-back offset that decodes cleanly and lands exactly on S
    best=S
    for k in range(2,160):
        o=S-k; ok=True
        while o<S:
            if ag[o] not in LEN: ok=False; break
            o+=LEN[ag[o]]
        if ok and o==S: best=S-k
    return best
BADGES={0x820:'Boulder',0x821:'Cascade',0x822:'Thunder',0x823:'Rainbow',
        0x824:'Soul',0x825:'Marsh',0x826:'Volcano',0x827:'Earth'}
# all setflag 0x82x sites
sites={}
for fid in BADGES:
    pat=bytes([0x29,fid&0xFF,fid>>8]); i=-1; L=[]
    while True:
        i=ag.find(pat,i+1)
        if i<0: break
        if 0x7F0000<=i<0x8D0000: L.append(i)
    sites[fid]=L
for fid,nm in BADGES.items():
    print(f"\n===== {nm} badge 0x{fid:X} =====")
    for S in sites[fid]:
        st=aligned_start(S); o=st; last_msg=None; br_after_msg=[]; trace=[]
        while o<=S:
            op=ag[o]
            if op in MSG:
                p=pf(o+ (2 if op==0x0F else 1)); last_msg=(o,txt(p) if p else '')
                trace.append(f"msg@{o:#x}\"{(txt(p) if p else '')[:24]}\"")
            elif op in BR:
                trace.append(f"BRANCH({'goto_if' if op==0x06 else 'call_if'})@{o:#x}")
                if last_msg is not None: br_after_msg.append(o)
            elif op==0x29 and o==S:
                trace.append(f"SETFLAG 0x{fid:X}@{o:#x}")
            o+=LEN.get(op,1)
            if op in TERM and o<=S: last_msg=last_msg; # goto/end resets straight-line; note it
        verdict = "SPLIT-RISK: branch between last msg and setflag" if br_after_msg else "coupled (no branch msg->setflag)"
        print(f"  setflag@{S:#x} block[{st:#x}..]: {verdict}")
        print(f"    {' | '.join(trace[-7:])}")
