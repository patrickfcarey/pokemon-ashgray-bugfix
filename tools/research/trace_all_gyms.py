import struct
ag = open('../ashgray-fork/rom/ashgray.gba', 'rb').read(); ROM = 0x08000000; LIT = 0x5524C
def pf(o):
    if o+4 > len(ag): return None
    v = struct.unpack('<I', ag[o:o+4])[0]
    return v-ROM if 0x08000000 <= v < 0x08000000+len(ag) else None
def plist(off, cap):
    r=[]
    for k in range(cap):
        p=pf(off+4*k)
        if p is None: break
        r.append(p)
    return r
