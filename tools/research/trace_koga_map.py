def reach(entry, targets, cap=4000):
    seen=set(); stack=[entry]; hit=set()
    while stack and len(seen) < cap:
        o = stack.pop()
        while 0 <= o < len(ag):
            if o in targets: hit.add(o)
            if o in seen: break
            seen.add(o)
            op = ag[o]
            if op == 0x5C:  # trainerbattle: queue its pointers, then stop this path
                typ = ag[o+1]
                for k in range(6, TBLEN.get(typ,14)):
                    p = pf(o+k)
                    if p is not None and ag[p] in LEN: stack.append(p)
                break
            if op not in LEN: break
            if op in PTR_AT:
                p = pf(o+PTR_AT[op])
                if p is not None: stack.append(p)
            if op in TERM: break
            o += LEN[op]
    return hit

